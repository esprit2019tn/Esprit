package com.esprit.Entity;

public class Comment {

	public Comment() {
	}
	private int idCom;
	private String text;

    public int getIdCom() {
        return idCom;
    }

    public void setIdCom(int idCom) {
        this.idCom = idCom;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
        
}